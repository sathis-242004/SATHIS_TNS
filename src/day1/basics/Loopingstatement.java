package day1.basics;

public class Loopingstatement {

	public static void main(String[] args) {
		
		byte a = 100;
		byte b= -101;
		
		System.out.println("b range of byte is" +
				b+"a range of byte is "+a);
		
		short C= 32;
		short D= -36;
		System.out.println("D range of byte is" +
				D+"C range of byte is "+C);
		
		//int - 4bytes
		int E = 47;
		int F= -48;
		System.out.println("F range of byte is" +
				F+"E range of byte is "+E);
		
		//long - 8bytes
		long G= 7L;
		long H = -9L;
		
		System.out.println("H range of byte is" +
				H+"G range of byte is "+G);
		
		float f=24.243278345f;
		double d=46.4512345678902345678914f;
		System.out.println("float value is "+f+" double value is "+d);
		
		//boolean 
		boolean flag=false;
		System.out.println("boolean value is "+flag);
	}
}

